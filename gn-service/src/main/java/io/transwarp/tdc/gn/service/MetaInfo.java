package io.transwarp.tdc.gn.service;

public class MetaInfo {

    private String type;
    private String url;

    public MetaInfo() {
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }
}
